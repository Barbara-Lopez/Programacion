    private void bConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bConfirmarActionPerformed
        bAceptar.setEnabled(true);
        try{    
            switch(opcion){
                case 'e':
                    Ejercicio22.buscar(tfNombre.getText());
                    tfLugar.setText( Ejercicio22.lugar());
                    tfFecha.setText( Ejercicio22.fecha());
                    tfHoraFin.setText(Ejercicio22.horaI());
                    tfHoraInicio.setText(Ejercicio22.horaF());
                    tfAforo.setText(Ejercicio22.aforo());
                    break;
                case 'a':
                    Ejercicio22.buscar(tfNombre.getText());
                    tfLugar.setText( Ejercicio22.lugar());
                    tfFecha.setText( Ejercicio22.fecha());
                    tfHoraFin.setText(Ejercicio22.horaI());
                    tfHoraInicio.setText(Ejercicio22.horaF());
                    tfAforo.setText(Ejercicio22.aforo());
                    tfLugar.setEnabled(true);
                    tfFecha.setEnabled(true);
                    tfHoraFin.setEnabled(true);
                    tfHoraInicio.setEnabled(true);
                    tfAforo.setEnabled(true);
                    tfNombre.setEnabled(false); 
                    bConfirmar.setEnabled(false); 
                    break;
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this, "No se encuentra el evento");
        }
    }//GEN-LAST:event_bConfirmarActionPerformed

