    private void bAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAceptarActionPerformed
        try{    
            switch(opcion){
                case 'g':
                    Ejercicio22.guardar(tfNombre.getText(),tfLugar.getText(),tfFecha.getText(),tfHoraInicio.getText(),tfHoraFin.getText(),tfAforo.getText());
                    Ejercicio22.inicio();
                    break;
                case 'e':
                    Ejercicio22.eliminar();
                    Ejercicio22.inicio();
                    break;
                case 'a':
                    Ejercicio22.actualizar(tfNombre.getText(),tfLugar.getText(),tfFecha.getText(),tfHoraInicio.getText(),tfHoraFin.getText(),tfAforo.getText());
                    Ejercicio22.inicio();
                    break;
            }
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        } catch (Exception e) {
             JOptionPane.showMessageDialog(this, "Problemas al hacer la acción a realizar");
        }
    }//GEN-LAST:event_bAceptarActionPerformed

